'use strict';


var 標記 = {
  不轉換: '{}',
  預設: '[]'
}


var 復原表 = {
  /**
   *  【格式】
   *    ・簡化字（規範字） ->  日本漢字（新字體）
   */
}


function 生成復原表 () {
  var 中文繁化表 = Object.assign({}, 繁化表, 單向繁化表)
  var 交集表 = 繁化表新字體交集表
  var 增補表 = 日文復原增補一對一表
  復原表 = filter(中文繁化表, 簡化字 => 交集表.不存在(簡化字))
  for ( let 簡化字 of Object.keys(增補表) ) {
    復原表[簡化字] = 增補表[簡化字]
  }
}


生成復原表()
var 復原規則 = new 轉換規則(
  復原表, 日文復原一對多表, {}, {}
)


function 復原(字串) {
  var 文 = new 文章(文章.生成字表(字串, 復原規則, 標記), 復原規則)
  return 文
}

